package com.test1;

public class Contact {
	private String id;
	private String firstName;
	private String emailId;
	private String lastName;
	private String updateFlag;
	private String addressId;
	private String orgUnit;
	private String phone;
	private String fax;
	private boolean isPrimary;
	private boolean isActive;
	private String creationDate;
	private String modifiedDate;
	private String entityState;

	public Contact() {

	}

	public Contact(String id, String firstName, String emailId, String lastName, String updateFlag, String addressId,
			String orgUnit, String phone, String fax, boolean isPrimary, boolean isActive, String creationDate,
			String modifiedDate, String entityState) {
		super();
		this.id = id;
		this.firstName = firstName;
		this.emailId = emailId;
		this.lastName = lastName;
		this.updateFlag = updateFlag;
		this.addressId = addressId;
		this.orgUnit = orgUnit;
		this.phone = phone;
		this.fax = fax;
		this.isPrimary = isPrimary;
		this.isActive = isActive;
		this.creationDate = creationDate;
		this.modifiedDate = modifiedDate;
		this.entityState = entityState;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getFirstName() {
		return firstName;
	}

	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}

	public String getEmailId() {
		return emailId;
	}

	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}

	public String getLastName() {
		return lastName;
	}

	public void setLastName(String lastName) {
		this.lastName = lastName;
	}

	public String getUpdateFlag() {
		return updateFlag;
	}

	public void setUpdateFlag(String updateFlag) {
		this.updateFlag = updateFlag;
	}

	public String getAddressId() {
		return addressId;
	}

	public void setAddressId(String addressId) {
		this.addressId = addressId;
	}

	public String getOrgUnit() {
		return orgUnit;
	}

	public void setOrgUnit(String orgUnit) {
		this.orgUnit = orgUnit;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getFax() {
		return fax;
	}

	public void setFax(String fax) {
		this.fax = fax;
	}

	public boolean isPrimary() {
		return isPrimary;
	}

	public void setPrimary(boolean isPrimary) {
		this.isPrimary = isPrimary;
	}

	public boolean isActive() {
		return isActive;
	}

	public void setActive(boolean isActive) {
		this.isActive = isActive;
	}

	public String getCreationDate() {
		return creationDate;
	}

	public void setCreationDate(String creationDate) {
		this.creationDate = creationDate;
	}

	public String getModifiedDate() {
		return modifiedDate;
	}

	public void setModifiedDate(String modifiedDate) {
		this.modifiedDate = modifiedDate;
	}

	public String getEntityState() {
		return entityState;
	}

	public void setEntityState(String entityState) {
		this.entityState = entityState;
	}

}
